"""
===========================================================
 FILE: convergence_plot.py
 MODULE: Plot convergence curves from saved CSV data
===========================================================

PURPOSE
-------
This script reads convergence histories generated by:

    experiments/run_convergence_data.py

and produces a publication-ready convergence plot.

Input:
    results/v0/convergence_histories.csv

Output:
    results/v0/convergence_plot_mean.png
===========================================================
"""

import os
import pandas as pd
import matplotlib.pyplot as plt


# ===========================================================
# PATHS
# ===========================================================
CSV_PATH = "results/v0/convergence_histories.csv"
SAVE_PATH = "results/v0/convergence_plot_mean.png"


# ===========================================================
# LOAD DATA
# ===========================================================
def load_convergence_data(csv_path=CSV_PATH):

    if not os.path.exists(csv_path):
        raise FileNotFoundError(
            f"Convergence data file not found: {csv_path}\n"
            "Please run: python -m experiments.run_convergence_data"
        )

    df = pd.read_csv(csv_path)

    required_columns = {
        "run",
        "seed",
        "iteration",
        "algorithm",
        "best_fitness"
    }

    missing = required_columns - set(df.columns)

    if missing:
        raise ValueError(
            f"Missing columns in convergence CSV: {missing}"
        )

    return df


# ===========================================================
# SUMMARY STATISTICS
# ===========================================================
def compute_summary(df):

    summary = (
        df
        .groupby(["algorithm", "iteration"])["best_fitness"]
        .agg(["mean", "std"])
        .reset_index()
    )

    return summary


# ===========================================================
# PLOT
# ===========================================================
def plot_convergence_from_csv(
        csv_path=CSV_PATH,
        save_path=SAVE_PATH,
        show_std=False
):

    os.makedirs("results/v0", exist_ok=True)

    plt.style.use("default")

    df = load_convergence_data(csv_path)
    summary = compute_summary(df)

    fig, ax = plt.subplots(
        figsize=(11, 6.5),
        facecolor="white"
    )

    ax.set_facecolor("white")

    styles = {
        "FOA": {
            "marker": "o",
            "label": "FOA (Proposed)"
        },
        "PSO": {
            "marker": "s",
            "label": "PSO"
        },
        "Random Search": {
            "marker": "^",
            "label": "Random Search"
        },
        "Greedy": {
            "marker": "D",
            "label": "Greedy"
        }
    }

    final_text = []

    for algorithm, style in styles.items():

        data = summary[
            summary["algorithm"] == algorithm
        ]

        x = data["iteration"].values
        mean = data["mean"].values
        std = data["std"].fillna(0).values

        ax.plot(
            x,
            mean,
            marker=style["marker"],
            markevery=3,
            linewidth=2.5,
            markersize=5,
            label=style["label"]
        )

        if show_std:
            ax.fill_between(
                x,
                mean - std,
                mean + std,
                alpha=0.08
            )

        final_text.append(
            f"{style['label']}: {mean[-1]:.1f} ± {std[-1]:.1f}"
        )

    ax.set_xlabel(
        "Iteration",
        fontsize=12
    )

    ax.set_ylabel(
        "Average Best Fitness",
        fontsize=12
    )

    ax.set_title(
        "Average Convergence over 30 Independent Runs (N=20, M=10)",
        fontsize=14,
        fontweight="bold"
    )

    ax.grid(
        True,
        linestyle="--",
        alpha=0.45
    )

    ax.legend(
        loc="lower right",
        fontsize=10,
        frameon=True
    )

    summary_text = (
        "Final average best fitness at iteration 50\n"
        + " | ".join(final_text)
    )

    fig.text(
        0.5,
        0.02,
        summary_text,
        ha="center",
        fontsize=10,
        bbox={
            "facecolor": "white",
            "edgecolor": "black",
            "boxstyle": "round,pad=0.5",
            "alpha": 0.95
        }
    )

    fig.tight_layout(
        rect=[0, 0.08, 1, 1]
    )

    fig.savefig(
        save_path,
        dpi=300,
        bbox_inches="tight",
        facecolor="white"
    )

    plt.close(fig)

    print(f"Convergence plot saved to: {save_path}")


# ===========================================================
# ENTRY POINT
# ===========================================================
if __name__ == "__main__":

    plot_convergence_from_csv(
        csv_path=CSV_PATH,
        save_path=SAVE_PATH,
        show_std=False
    )